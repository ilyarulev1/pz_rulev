 # Дан список А размера N. Сформировать новый список В того же размера, элементы
#которого определяются следующим образом: Вк = 2* Ак, если Ак < 5, Ак/2 в противном
#случае.
N = input("Введите размер списка N: ")
while type(N) != int:
    try:
        N = int(N)
    except ValueError:
        print('Введите ЧИСЛО!')
        N = input('Введи число: ')

# Создаем список A из N чисел, введенных пользователем
A = []
print("Введите", N, "элементов списка:")
for i in range(N):
    num = input(f"Элемент {i + 1}: ")
    while type(num) != int:
        try:
            num = int(num)
        except ValueError:
            print('Введите ЧИСЛО!')
            num = input(f"Элемент {i + 1}: ")
    A.append(num)

# Создаем новый список B, преобразуя элементы из списка A
B = []
for x in A:
    if x < 5:
        B.append(2 * x)  # Если элемент меньше 5, умножаем на 2
    else:
        B.append(x / 2)  # Иначе делим на 2

# Выводим результаты
print("Исходный список A:", A)
print("Новый список B:", B)